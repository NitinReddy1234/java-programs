class HumanBeing
{
	public static void main(String a[])
	{
		String name="Nitin Reddy";
		int age=23;
		float height=5.8F;  // F is used to indicate its a float
		double salary=30.00D;   // D is used to indicate its a double 
		long phoneNumber=9876543210L;  // L is used to indicate its a long 
		short marks=95;
		char grade='A';

	System.out.println("Name is :"+name);
	System.out.println("Age is :"+age);
	System.out.println("Height is :"+height);
	System.out.println("Salary is :"+salary+"LPA");
	System.out.println("PhoneNumber is :"+phoneNumber);
	System.out.println("Marks is :"+marks);
	System.out.println("Grade is :"+grade);
	
	}

}



 